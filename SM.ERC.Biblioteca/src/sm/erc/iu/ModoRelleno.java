package sm.erc.iu;

/**
 * Representa modos de relleno de figuras.
 * @author Elena Romero Contreras
 * @version 1.0
 */
public enum ModoRelleno {
    /**
     * Sin relleno
     */
    SIN, 
    /**
     * Relleno liso
     */
    LISO,
    /**
     * Degradado horizontal
     */
    DEGRADADO_H, 
    /**
     * Degradado vertical
     */
    DEGRADADO_V, 
    /**
     * Degradado diagonal
     */
    DEGRADADO_D, 
    /**
     * Degradado radial
     */
    RADIAL,
}
